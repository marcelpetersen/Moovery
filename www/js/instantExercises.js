var App = angular.module('ionicApp');

App.directive("topmVideogularDirective", function ($state) {
	return {
		restrict: 'E',
		scope: {
		data: "=",
			config: "="
		},
		template: '<div class="instant-exercise-buttons"><videogular vg-plays-inline="true" vg-player-ready="onPlayerReady($API)"><vg-media vg-src="data.source" vg-native-controls="false" vg-loop="true"></vg-media><vg-poster vg-url="data.image"></vg-poster></videogular><button class="button button-small button-stable" translate="INSTANT_EXERCISE_BUTTON_TEXT_PREVIEW" ng-click="playVideo()">preview</button><button class="button button-small button-balanced" translate="INSTANT_EXERCISE_BUTTON_TEXT_EXERCISE" ng-click="goToExercise(data, 0)">exercise</button></div>',
		link: function (scope, state) {

			scope.API = null;
			
			scope.onPlayerReady = function(API) {
				scope.API = API;
			};

			scope.playVideo = function(){
				scope.API.play();
			};

			scope.goToExercise = function(data, index){
				$state.go('tabs.exercise', { exerciseData: [data], index: index});	
			};
		}
	};
});

